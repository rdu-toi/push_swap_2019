#include "libft.h"

char	*ft_strrev(char *s)
{
	int		i;
	int		j;
	char	tmp;

	i = (int)ft_strlen(s) - 1;
	j = 0;
	while (j < (i / 2))
	{
		tmp = s[j];
		s[j] = s[i - j];
		s[i - j] = tmp;
		j++;
	}
	return (s);
}
