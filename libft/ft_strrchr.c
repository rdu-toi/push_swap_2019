#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		i;
	char	d;

	i = 0;
	d = (char)c;
	while (s[i] != '\0')
		i++;
	while (i >= 0)
	{
		if (s[i] == d)
			return ((char*)&s[i]);
		i--;
	}
	return (0);
}
